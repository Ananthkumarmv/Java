class DeclaringStrings{
	
	public static void main(String []args){
		
		// Declaring string without using new operator
		String s = "Ananth";
		
		// prints the string
		System.out.println("String s = " + s);
		
		// Declaring String using new operator 
		String s1 = new String("Ananth");
		
		// prints the string
		System.out.println("String s = " + s1);
		

	}
}
